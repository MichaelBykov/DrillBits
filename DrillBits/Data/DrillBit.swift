/// An enumerator for all the possible (supported) drill bits
enum DrillBit {
	/// Twist drill bit
	case Twist
	/// Bullet pilot-point drill bit
	case BulletPilotPoint
	/// Brad-point bit
	case BradPoint
	/// Forstner bit
	case Forstner
	/// Glass-and-tile drill bit
	case GlassAndTile
	/// Hole saw
	case HoleSaw
	/// Multi spur drill bit
	case MultiSpur
	/// Spade bit
	case Spade
	/// Spade bit with spurs
	case SpadeWithSpurs
	/// Power bore drill bit
	case PowerBore
	/// Circle cutter bit
	case CircleCutter
}
